  function openImage(event) {
    let images = document.querySelectorAll('img[onclick="openImage(event)"]');
    let currentIndex = Array.from(images).findIndex((img) => img === event.target);

    document.onkeydown = function(e) {
      switch (e.key) {
        case "ArrowLeft":
          navigateImage(-1);
          break;
        case "ArrowRight":
          navigateImage(1);
          break;
      }
    };

    showImage(event.target.src, currentIndex, images.length);
  }

  function showImage(src, currentIndex, totalImages) {
