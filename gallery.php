    fullView.style.display = "flex";

    if (currentIndex === 0) {
      leftArrow.style.display = "none";
    } else {
      leftArrow.style.display = "block";
    }

    if (currentIndex === totalImages - 1) {
      rightArrow.style.display = "none";
    } else {
      rightArrow.style.display = "block";
    }

    window.onclick = function(event) {
      if (event.target === fullView) {
        closeImage();
      }
    };
  }

  function closeImage() {
    let fullView = document.getElementById("fullView");
    fullView.style.display = "none";
    document.onkeydown = null;
    window.onclick = null;
  }

  function navigateImage(direction) {
    let images = document.querySelectorAll('img[onclick="openImage(event)"]');
    let currentImage = document.getElementById("fullImage").src;
    let currentIndex = Array.from(images).findIndex((img) => img.src === currentImage);
    let newIndex = (currentIndex + direction + images.length) % images.length;
    let newImage = images[newIndex].src;
    showImage(newImage, newIndex, images.length);
  }
